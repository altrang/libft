#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	int		tmp;

	i = 0;
	tmp = (int)n;
	while (i < tmp)
	{
		((char*)s)[i] = 0;
		i++;
	}
}
