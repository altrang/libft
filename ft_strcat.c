#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		pos;

	i = ft_strlen(s1);
	pos = 0;
	while (s2[pos])
	{
		s1[i] = s2[pos];
		pos++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
